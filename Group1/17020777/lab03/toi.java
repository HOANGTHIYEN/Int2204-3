public class toi {
    private String vocdang;
    private String ngoaihinh;
    private String tinhcach;

    /**
     * @return the vocdang
     */
    public String getVocdang() {
        return vocdang;
    }

    /**
     * @param vocdang the vocdang to set
     */
    public void setVocdang(String vocdang) {
        this.vocdang = vocdang;
    }

    /**
     * @return the ngoaihinh
     */
    public String getNgoaihinh() {
        return ngoaihinh;
    }

    /**
     * @param ngoaihinh the ngoaihinh to set
     */
    public void setNgoaihinh(String ngoaihinh) {
        this.ngoaihinh = ngoaihinh;
    }

    /**
     * @return the tinhcach
     */
    public String getTinhcach() {
        return tinhcach;
    }

    /**
     * @param tinhcach the tinhcach to set
     */
    public void setTinhcach(String tinhcach) {
        this.tinhcach = tinhcach;
    }
    
}
