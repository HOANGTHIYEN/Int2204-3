public class LopHoc {
        private String TenPhong;
	private String Diadiem;
	private String Succhua;
	private String Loaiphong;

    /**
     * @return the TenPhong
     */
    public String getTenPhong() {
        return TenPhong;
    }

    /**
     * @param TenPhong the TenPhong to set
     */
    public void setTenPhong(String TenPhong) {
        this.TenPhong = TenPhong;
    }

    /**
     * @return the Diadiem
     */
    public String getDiadiem() {
        return Diadiem;
    }

    /**
     * @param Diadiem the Diadiem to set
     */
    public void setDiadiem(String Diadiem) {
        this.Diadiem = Diadiem;
    }

    /**
     * @return the Succhua
     */
    public String getSucchua() {
        return Succhua;
    }

    /**
     * @param Succhua the Succhua to set
     */
    public void setSucchua(String Succhua) {
        this.Succhua = Succhua;
    }

    /**
     * @return the Loaiphong
     */
    public String getLoaiphong() {
        return Loaiphong;
    }

    /**
     * @param Loaiphong the Loaiphong to set
     */
    public void setLoaiphong(String Loaiphong) {
        this.Loaiphong = Loaiphong;
    }
}
