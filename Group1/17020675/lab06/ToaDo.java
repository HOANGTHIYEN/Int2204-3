public class ToaDo {
    private double toadoX;
    private double toadoY;

    public ToaDo() {
    }

    public ToaDo(double toadoX, double toadoY) {
        this.toadoX = toadoX;
        this.toadoY = toadoY;
    }

    public double getToadoX() {
        return toadoX;
    }

    public void setToadoX(double toadoX) {
        this.toadoX = toadoX;
    }

    public double getToadoY() {
        return toadoY;
    }

    public void setToadoY(double toadoY) {
        this.toadoY = toadoY;
    }
}
