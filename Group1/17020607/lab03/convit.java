public class convit {
    private String tuoi;
    private String cannang;

    public String getTuoi() {
        return tuoi;
    }

    public String getCannang() {
        return cannang;
    }

    public void setTuoi(String tuoi) {
        this.tuoi = tuoi;
    }

    public void setCannang(String cannang) {
        this.cannang = cannang;
    }

    public convit(String tuoi, String cannang) {
        this.tuoi = tuoi;
        this.cannang = cannang;
    }
    
}
