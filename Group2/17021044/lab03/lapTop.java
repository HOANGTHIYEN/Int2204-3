public class lapTop {
    private String ten, hang;
    private int gia, namBaoHanh;

    public lapTop(String ten, String hang, int gia, int namBaoHanh) {
        this.ten = ten;
        this.hang = hang;
        this.gia = gia;
        this.namBaoHanh = namBaoHanh;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getHang() {
        return hang;
    }

    public void setHang(String hang) {
        this.hang = hang;
    }

    public int getGia() {
        return gia;
    }

    public void setGia(int gia) {
        this.gia = gia;
    }

    public int getNamBaoHanh() {
        return namBaoHanh;
    }

    public void setNamBaoHanh(int namBaoHanh) {
        this.namBaoHanh = namBaoHanh;
    }
    
}
