public class studentUET{
    private String ten;
    private int tuoi;
    private String class_name;

    public void set_ten(String ten){
        this.ten = ten;
    }

    public void set_tuoi(int tuoi){
        this.tuoi = tuoi;
    }

    public void set_class_name (String class_name){
        this.class_name = class_name;
    }

    public String get_ten() {
        return this.ten;
    }

    public int get_tuoi() {
        return this.tuoi;
    }

    public String get_class_naem() {
        return this.class_name;
    }
}